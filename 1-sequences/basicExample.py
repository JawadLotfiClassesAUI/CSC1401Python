# Prompt the user to enter their age and convert the input to an integer
age = int(input("Enter your age:"))
# Prompt the user to enter their height and convert the input to a float
height = float(input("Enter your height:"))
# Prompt the user to enter their name
name = input("Enter your name:")

# Display the user's name, age, and height
print(f"{name} is {age} years old and has a height of {height} meter")